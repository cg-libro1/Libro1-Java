public class C1P4 {
      public static void main(String[] args) {
            System.out.printf("%.2f %n", 12.37689456);
            System.out.printf("\t%.4f \n", 12.37689456);
            System.out.printf("%+.2f %n", 4.759865);
            System.out.printf("%+d %n", 185);
            System.out.printf("%8d %n", 185);
            System.out.printf("%-7d %n", 185);
            System.out.printf("%+-8d %n", 185);
    }    
}
